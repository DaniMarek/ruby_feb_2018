# Go ahead and try the following methods:

# .include?(value) => true or false
puts ("1".."55").include?("15")
puts ("a".."z").include?("D")

# .last => returns the last object in range
puts (1..55).last(3)
puts (1...55).last(3)

# .max => returns the maximum value in range
puts (1..55).max
puts (1...55).max 

# .min => returns the minimum value in range
puts (1..55).min
puts (1...55).min
